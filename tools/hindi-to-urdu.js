// Generate ur.js
function esc(s) {
  if (!s) return '';
  return s.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n');
}

function formatArr(arr, indent) {
  if (!arr || arr.length === 0) return '[]';
  let lines = arr.map(item => `${indent}  "${esc(item)}"`);
  return `[\n${lines.join(',\n')}\n${indent}]`;
}

