function transliterate(text) {
  if (!text) return text;

  // Try word-level replacement first
  let words = text.split(/(\s+|[,،۔.!?\-:;\(\)\/\d]+)/);
  let result = words.map(word => {
    if (wordMap[word]) return wordMap[word];
    if (/^[\s,،۔.!?\-:;\(\)\/\d]+$/.test(word)) return word;
    if (!/[\u0900-\u097F]/.test(word)) return word; // no Devanagari

    // Character-level transliteration
    let out = '';
    let i = 0;
    while (i < word.length) {
      // Check two-char sequences first (nukta forms like ड़, ढ़)
      if (i + 1 < word.length) {
        let two = word[i] + word[i+1];
        if (consonants[two]) { out += consonants[two]; i += 2; continue; }
        if (vowels[two]) { out += vowels[two]; i += 2; continue; }
      }

      let ch = word[i];
      if (consonants[ch]) { out += consonants[ch]; i++; continue; }
      if (vowels[ch]) { out += vowels[ch]; i++; continue; }
      if (matras[ch] !== undefined) { out += matras[ch]; i++; continue; }
      if (ch === '़') { i++; continue; } // nukta handled above
      if (ch === '्') { i++; continue; } // halant
      out += ch;
      i++;
    }
    return out;
  }).join('');

  // Replace Devanagari danda with Urdu full stop
  result = result.replace(/।/g, '۔');

  return result;
}

function transliterateArray(arr) {
  if (!arr) return arr;
  return arr.map(item => transliterate(item));
}

