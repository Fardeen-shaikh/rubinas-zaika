function translateToMarathi(text) {
  if (!text) return text;
  let result = text;
  for (const [hi, mr] of replacements) {
    // Use global replace
    result = result.split(hi).join(mr);
  }
  return result;
}

function translateArray(arr) {
  if (!arr) return arr;
  return arr.map(item => translateToMarathi(item));
}

// Build Marathi recipes
const mrRecipes = {};
for (const [id, recipe] of Object.entries(hiData.recipes)) {
  mrRecipes[id] = {
    title: translateToMarathi(recipe.title),
    description: translateToMarathi(recipe.description),
    intro: translateToMarathi(recipe.intro),
    ingredients: translateArray(recipe.ingredients),
    method: translateArray(recipe.method),
    outro: translateToMarathi(recipe.outro),
    tips: translateArray(recipe.tips),
  };
}

console.log(`Translated ${Object.keys(mrRecipes).length} recipes to Marathi`);

// Generate mr.js
function esc(s) {
  if (!s) return '';
  return s.replace(/\\/g, '\\\\').replace(/"/g, '\\"').replace(/\n/g, '\\n');
}

function formatArr(arr, indent) {
  if (!arr || arr.length === 0) return '[]';
  let lines = arr.map(item => `${indent}  "${esc(item)}"`);
  return `[\n${lines.join(',\n')}\n${indent}]`;
}

